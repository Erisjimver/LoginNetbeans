      void acceder(String usuario, String pas)
    {
       String u="",p="";
       String sql="select username, password from users where username='"+usuario+"' and password='"+pas+"'";         
        try {
            st = c.createStatement();
            r = st.executeQuery(sql);
            while(r.next())
            {
               u=r.getString(1);
               p=r.getString(2); 
            }
            if(u.equals(usuario)&&p.equals(pas))
            {
                lblestado.setText("Conexion exitosa");               
            }
            if(!u.equals(usuario)&&!p.equals(pas))
            {
                lblestado.setText("Datos incorrectos");               
            }

        }
        catch(SQLException ex) 
        {
            System.out.println("Error: "+ex);
        }
       
        }

  
